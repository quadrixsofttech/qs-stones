const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const ConferenceRoomReservationSchema = new Schema({
  column: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  start_time: {
    type: String,
    required: true,
  },
  end_time: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  color: {
    type: String,
    required: true,
  },
  userId: {
    type: Schema.Types.ObjectId,
    required: true,
  },
});

module.exports = mongoose.model(
  'conference-room-reservation',
  ConferenceRoomReservationSchema
);
